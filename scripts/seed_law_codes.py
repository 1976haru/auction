def seed() -> int:
    init_db()
    conn = get_connection()
    c = conn.cursor()
    for sido, sigungu, code, price, yield_ in LAW_CODES:
        c.execute("""
            INSERT INTO law_codes (sido, sigungu, code, avg_apt_price_per_m2, avg_rent_yield)
            VALUES (?, ?, ?, ?, ?)
            ON CONFLICT(code) DO UPDATE SET
                sido=excluded.sido, sigungu=excluded.sigungu,
                avg_apt_price_per_m2=excluded.avg_apt_price_per_m2,
                avg_rent_yield=excluded.avg_rent_yield
        """, (sido, sigungu, code, price, yield_))
    conn.commit()
    n = conn.execute("SELECT COUNT(*) FROM law_codes").fetchone()[0]
    conn.close()
    log.info(f"[seed] law_codes {len(LAW_CODES)}건 입력 (총 {n})")
    return len(LAW_CODES)


